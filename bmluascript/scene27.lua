--	10016 阎罗
gNPCQuestTable[10016] =
function(npc, player)
		player:ShowQuestDlg(npc, 0, 0)
end

gNPCButtonTable[10016] =
function(bid, npc, player)
	if bid == 2 then
		player:ShowQuestDlg(npc, 0, 1)

	elseif bid == 3 then
		player:ClearAllItem()
		player:FlyToHome()
		player:HideQuestDlg()

	end
end



